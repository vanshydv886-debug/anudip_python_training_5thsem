#function to calculate area of rectangle
def perimeter_rectangle(length , breadth):

    #calcuation of perimeter of rectangle
    perimeter = 2 *(length + breadth)
    #return the calculated perimeter 
    return perimeter

# function to calculate perimeter of square
def perimeter_square(side):

    # calculation of perimeter
    perimeter = 4 * side

    # return the calculated perimeter
    return perimeter

# function to calculate circumference of circle
def perimeter_circle(radius):

    # value of pi
    pi = 3.14

    # calculation of circumference
    perimeter = 2 * pi * radius

    # return the calculated perimeter
    return perimeter